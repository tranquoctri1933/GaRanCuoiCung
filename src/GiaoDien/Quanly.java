    void filltablegh() {
        if (row == -1) {
            MsgBox.alert(this, "Vui lòng chọn sản phẩm!");
        } else {
            String a = MsgBox.prompt(this, "Nhập số lượng!");
            row = tblbanhang.getSelectedRow();
            sanpham sp = splist.get(row);
            DefaultTableModel model = (DefaultTableModel) tblgiohang.getModel();
            try {
                Object[] row = {
                    sp.getMasp(),
                    sp.getTensp(),
                    sp.getDongia(),
                    a
                };
                // thàom row vào model
                model.addRow(row);
                spdao.updategiohangtru(Integer.parseInt(a), masp);
                tongtien +=spdao.tongtien(dongia,Integer.parseInt(a),giamgia , masp);
                lbltongtien.setText(String.valueOf(tongtien));

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

    void filltablebh() {
        DefaultTableModel model = (DefaultTableModel) tblbanhang.getModel();
        model.setRowCount(0);
        try {
            String keyword = txttiemkiem.getText();
            List<sanpham> list = spdao.selectByKeyword(keyword);
            for (sanpham sp : list) {
                Object[] row = {
                    sp.getMasp(),
                    sp.getTensp(),
                    sp.getDongia(),
                    sp.getGiamgia(),
                    sp.getSoluongton()
                };
                model.addRow(row);
                splist.add(sp);
            }
        } catch (Exception e) {
            MsgBox.alert(this, "Lỗi truy vấn dữ liệu!");
        }
    }

